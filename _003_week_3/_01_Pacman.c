void main() {
	system("mode con: cols=62 lines=48");
	char ch;

	for (int i = 0; i < sizeof(map) / sizeof(map[31]); i++)
	{
		for (int j = 0; j < sizeof(map[31]) / sizeof(map[31][31]); j++)
		{
			ch = map[i][j];
			switch (ch)
			{
			case 0:
				printf("  ");
				break;
			case 1:
				printf("■");
				break;
			}
		}
	}

	CursorView(0);
	GotoXY(x, y);
	printf("○");
	while (1)
	{
		if (_kbhit())
		{
			int nkey = _getch();
			if (nkey == ARROW) 
			{
				nkey = _getch();
				switch (nkey) 
				{
				case UP:
					system("cls");
					GotoXY(x, --y);
					printf("○");
					break;
				case LEFT:
					system("cls");
					GotoXY(x = x - 2, y);
					printf("○");
					break;
				case RIGHT:
					system("cls");
					GotoXY(x = x + 2, y);
					printf("○");
					break;
				case DOWN:
					system("cls");
					GotoXY(x, ++y);
					printf("○");
					break;
				}
			}
		}
	}
}