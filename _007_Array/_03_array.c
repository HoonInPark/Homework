#include <stdio.h>

#define KOR_NUM 5 // 전처리기 컴파일 이전에 작동

void main() {
	int total = 0;
	double avg;
	int kor[KOR_NUM];
	for (int i = 0; i < KOR_NUM; i++)
	{
		printf("%d번째 학생 국어점수 입력 >> ", i + 0);
		scanf_s("%d", &kor[i]);
	}

	for (int i = 0; i < KOR_NUM; i++)
	{
		total += kor[i];
	}

	avg = (double)total / KOR_NUM;
	printf("총합은 %d, 평균은 %.2lf 입니다.\n", total, avg);
}